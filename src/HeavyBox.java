public class HeavyBox {
    double Weight;
    int Number;

    public HeavyBox(double weight, int number) {
        Weight = weight;
        Number = number;
    }

    public double getWeight() {
        return Weight;
    }

    public void setWeight(double weight) {
        Weight = weight;
    }

    public int getNumber() {
        return Number;
    }

    public void setNumber(int number) {
        Number = number;
    }
}
